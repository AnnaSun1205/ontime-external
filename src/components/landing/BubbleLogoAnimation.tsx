interface BubbleLogoAnimationProps {
  onComplete: () => void;
  buttonRect: DOMRect | null;
}

interface Bubble {
  id: number;
  logo: typeof logos[number];
  x: number;
  y: number;
  delay: number;
  drift: number;
  scale: number;
}

export function BubbleLogoAnimation({ onComplete, buttonRect }: BubbleLogoAnimationProps) {
  const [bubbles, setBubbles] = useState<Bubble[]>([]);
  const [animating, setAnimating] = useState(false);
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!buttonRect) return;

    const centerX = buttonRect.left + buttonRect.width / 2;
    const centerY = buttonRect.top + buttonRect.height / 2;

    const shuffled = [...logos].sort(() => Math.random() - 0.5).slice(0, 10);

    const newBubbles: Bubble[] = shuffled.map((logo, i) => ({
      id: i,
      logo,
      x: centerX + (Math.random() - 0.5) * 80,
      y: centerY,
      delay: i * 80,
      drift: (Math.random() - 0.5) * 120,
      scale: 0.7 + Math.random() * 0.5,
    }));

    setBubbles(newBubbles);
    setAnimating(true);

    const timer = setTimeout(() => {
      onComplete();
    }, 1800);

    return () => clearTimeout(timer);
  }, [buttonRect, onComplete]);

  if (!animating || bubbles.length === 0) return null;

  return (
    <div ref={containerRef} className="fixed inset-0 z-50 pointer-events-none">
      {bubbles.map((bubble) => (
        <div
          key={bubble.id}
          className="absolute"
          style={{
            left: bubble.x,
            top: bubble.y,
            transform: `translate(-50%, -50%) scale(${bubble.scale})`,
            animation: `bubble-rise 1.4s ease-out ${bubble.delay}ms forwards`,
            opacity: 0,
            // Drift via custom property
            ["--drift" as string]: `${bubble.drift}px`,
          }}
        >
          <div className="bg-card rounded-2xl p-3 shadow-lg border border-border/40">
            {bubble.logo.svg}
          </div>
        </div>
      ))}
    </div>
  );
}
